		<h1>Patientenaufklärungsbögen für Menschen mit Sehbeeinträchtigung zugänglich machen</h1>
		<p>Patiententerminals mit Touchscreens sind heutzutage Alltag in Krankenhäusern, doch verfügen diese standardmäßig nicht über Hilfstechnologien wie Screenreader oder Vergrößerungssoftware und sind somit für sehgeschädigte Personen nicht bedienbar. Im Rahmen eines Projekts mit dem Fraunhofer Institut, der Papenmeier GmbH, der Thieme Compliance GmbH und dem Zentrum für blinde und sehbehinderte Studierende (BliZ) sollen Patiententerminals der neuen Generation für die genannte Personengruppe zugänglich gemacht werden, so dass Inhalte des Patiententerminals gelesen und auch Eingaben erfolgen können.</p>
		<br />
		<img src="biz/img/bilder/braille_montage_b.jpg" alt="Patiententerminals für Menschen mit Sehbeeinträchtigung zugänglich machen" title="Patiententerminals für Menschen mit Sehbeeinträchtigung zugänglich machen" />
		<br />
		<p>In diesem Zusammenhang liegt es nahe, Patientenaufklärungsbögen, die normalerweise nur auf Papier vorliegen und damit für sehgeschädigte Menschen schwer oder gar nicht zugänglich sind, so zu gestalten, dass sie eigenständig von ihnen gelesen, ausgefüllt und unterschrieben werden können. Auf diesem Wege soll es den sehgeschädigten Personen ermöglicht werden, über ihre eigenen Patientendaten zu verfügen, ohne die Hilfe Dritter in Anspruch zu nehmen.</p>
