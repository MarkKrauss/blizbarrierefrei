	<h1>So können Sie uns helfen - oder: was wir mit Ihrer Unterstützung erreichen können</h1>
<p>Viele Menschen, Firmen oder Einrichtungen haben uns bislang schon unterstützt. Ihre Zuwendungen in Form von Sachspenden (Büromöbel, Computer, ...), Geldspenden oder unentgeltlicher Tätigkeit haben uns in die Lage versetzt, unsere behinderten und chronisch kranken Studierenden zu betreuen. Wir verfügen, nicht zuletzt durch das Engagement der THM über einige sehgeschädigtengerecht ausgestattete PC-Arbeitsplätze in mehreren Räumen. Neben der Durchführung von Klausuren und Übungen stehen diese Räume mit den Geräten den Studierenden für Hausarbeiten o. ä. zur Verfügung.</p>
