	<p>Studiare a Gießen- il centro Bliz vi accompagna…</p></br>
	<p>
	Lo studio all’università è il primo grande passo verso l’autosufficienza di molti invalidi e malati cronici (disturbi della vista, dislessia, ansia, danni all’udito, autismo, malattie psichiche). Per la maggior parte di queste persone che arrivano in una scuola, è tutto sistemato. Dove ricevo informazioni, dove si seguono le lezioni, come posso ricevere materiale d’insegnamento adatto al mio handicap? Ci siamo occupati di queste e molte altre domande, accompagnando così i nostri studenti dall’inizio dei loro studi fino alla laurea e oltre. Altre informazioni su di noi sono sul nostro volantino del Bliz. Siamo anche presenti per gli studenti con handicap dell’università Justus-Liebig e ci sforziamo di aiutarli il più possibile. Potete informarvi anche sul nostro progetto „Hessisches - elektronisches Lernportal für chronisch Kranke und Behinderte (H-eLB) „ Portale d’insegnamento dell'Assia- elettronico per malati cronici e portatori di handicap (H-eLB)“.
	</p>
	</br>
	<p>
	Per qualunque desiderio e suggerimento dai nostri studenti per migliorarci, le nostre orecchie sono aperte. Siamo dell’opinione che gli studenti stessi sappiano ciò che è meglio per loro durante gli studi. Noi mettiamo semplicemente a loro disposizione i sussidi generalmente necessari come la risposta audio, la riga Braille o lettori per il display. Per i sussidi individualmente necessari parliamo direttamente con i nostri studenti e cerchiamo insieme una soluzione.
	</p></br></br>
	<h2>Per domande siamo volentieri a vostra disposizione</h2>
	<p>Non tutti sono nati per lottare soli. Ovviamente ognuno può tentare di cavarsela da solo con il proprio handicap. Spesso funziona, ma nella maggior parte dei casi, dei piccoli aiuti dalla nostra parte possono portare a grandi risultati che non ci si sarebbero aspettati. Perché dover allungare lo studio con complicazioni inutili, quando così si possono superare meglio e più velocemente?
	</p></br>
